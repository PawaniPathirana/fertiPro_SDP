                echo "<p>Price of TSP: Rs. " . $row['priceOfTSP'] . " </p>";
                echo "<p>Total Quantity: " . $row['totalQuantity'] . "kg </p>";
                echo "<p>Total Price: Rs. " . $row['totalPrice'] . "</p>";
                echo "<p>Status: Ordered</p>";
